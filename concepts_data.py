def all_concepts():
    """返回所有概念名列表."""
    return list(CONCEPTS.keys())


def stocks_of(concept):
    """获取某概念的成分股 [(ts_code, name), ...]."""
    return CONCEPTS.get(concept, [])


def find_concept(query):
    """模糊匹配概念名. 支持子串和标签.

    query="ai" → ["AI芯片", "AI 应用"] etc.
    """
    q = query.lower()
    matches = []
    for c in CONCEPTS:
        if q in c.lower() or q in c.replace(" ","").lower():
            matches.append(c)
    return matches


